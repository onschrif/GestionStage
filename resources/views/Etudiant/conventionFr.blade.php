<p>
    <strong>10.1 </strong>
    Rupture à l'initiative du stagiaire
</p>
<p>
    Le stagiaire peut rompre la convention de stage après avoir informé de sa
    décision son maître
</p>
<p>
    de stage ainsi que le responsable pédagogique.
</p>
<p>
    <strong>10.2 </strong>
    Suspension ou rupture pour raisons médicales
</p>
<p>
    Le stage peut être suspendu ou interrompu pour raisons médicales. Dans ce
    cas, un avenant
</p>
<p>
    comportant les aménagements requis ou la rupture de la convention de stage
    sera conclu.
</p>
<p>
    <strong>10.3 </strong>
    Rupture pour manquement à la discipline
</p>
