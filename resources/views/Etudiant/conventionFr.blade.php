    l'entreprise ;
</p>
<p>
    - à ne pas faire de copie illicite des logiciels informatique s appartenant
    à l'entreprise
</p>
<p>
    ni implanter dans les systèmes internes à l'entreprise des logiciels de
    provenance externe.
