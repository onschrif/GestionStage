    <strong>Il a été arrêté et convenu ce qui suit :</strong>
</p>
<p>
    <strong>ARTICLE 1(objet de la convention)</strong>
    : La présente convention règle les rapports entre ESPRIT, l’entreprise et
    l’élève pour ce qui concerne le <strong>stage</strong> que l’élève est
    appelé à effectuer au sein de l’entreprise.
</p>
<p>
    <strong></strong>
</p>
<p>
    <strong>ARTICLE 2 (objet du stage)</strong>
    - Le stage a pour objet essentiel la mise en pratique de l'enseignement
    dispensé à ESPRIT. Il s'effectue sous la direction d'un encadrant technique
    de l'entreprise.
</p>
<p>
    <strong>ARTICLE 3</strong>
    <strong>(programme)</strong>
    - Le programme du stage, annexé à la présente convention est proposé par
    l’entreprise et validé par ESPRIT, en fonction du programme général des
    études et de la spécialisation de l’élève.
</p>
<p>
    <strong>ARTICLE 4</strong>
    <strong>(statut de l’élève)</strong>
    - Pendant la durée de son séjour en entreprise, l’élève conserve son statut
    d'élève. Toutefois Il doit se conformer au règlement intérieur de
    l’entreprise.et à ses règles d’usage.
</p>
<p>
    <strong>ARTICLE 5 (résiliation) </strong>
</p>
<p>
    <strong>5.1 Rupture à l'initiative du stagiaire</strong>
</p>
<p>
    Le stagiaire peut rompre la convention de stage après avoir obtenu l'accord
    express et non équivoque du maître de stage et informé la direction des
    stages d'Esprit.
</p>
<p>
    <strong>5.2 Suspension ou rupture pour raisons médicales</strong>
</p>
<p>
    Le stage peut être suspendu ou interrompu pour raisons médicales. Dans ce
    cas, un avenant comportant les aménagements requis ou la rupture de la
    convention de stage sera conclu.
</p>
<p>
    <strong>5.3 Rupture pour manquement à la discipline</strong>
</p>
<p>
    En cas de manquement à la discipline de l'entreprise par le stagiaire, le
    chef d'entreprise se réserve le droit de mettre fin au stage après en avoir
    informé le responsable de l'établissement d'enseignement.
</p>
<p>
    .
</p>
<p>
    <strong>ARTICLE 6</strong>
    (protection sociale) – L’élève conserve sa protection sociale dans le cadre
    du contrat d’assurance souscrit par ESPRIT au profit de ses élèves. Par
    ailleurs, il est aussi bénéficiaire de la législation sur les accidents du
    travail, tant pour l'accident dans l'entreprise que pendant le trajet aller
    et retour, en Tunisie ou à l'étranger.
</p>
<p>
    En cas d'accident dans l'entreprise ou au cours du trajet, l'entreprise en
    informera immédiatement le Service de Scolarité d’ESPRIT.
</p>
<p>
    L'entreprise et l’élève doivent être assurés en matière de responsabilité
    civile, selon les dispositions réglementaires en vigueur.
</p>
<p>
    <strong>ARTICLE 7</strong>
    (rapport de stage) - A la fin du stage, l’élève déposera à ESPRIT un
    rapport de stage visé par l’entreprise. Un exemplaire de ce rapport devra
    être communiqué à l’entreprise dans le respect des droits d'auteur de
    l'élève.
</p>
<p>
    A la fin du stage, l’entreprise délivrera une attestation à l’élève
    précisant la nature du stage et sa durée.
</p>
<p>
    <strong>ARTICLE 8</strong>
    (confidentialité) – L’élève sera tenu, aussi bien pendant la durée du stage
    qu'après celui-ci, à observer le secret professionnel à l'égard des tiers
    pour tout ce qui concerne son activité pendant son stage et d'une façon
    plus générale pour tout ce dont il aurait eu connaissance directement ou
    indirectement à l'occasion de celui-ci, le contenu des documents qu'il
    aurait rédigés lui-même, y compris le rapport du stage, ou qui pourrait lui
    être remis par l'entreprise restant bien entendu la propriété 0de celle-ci.
</p>
<p>
    <strong>ARTICLE 9 </strong>
    (consentement) - La présente convention est préalablement portée à la
    connaissance de l’élève ou de son représentant légal, s'il est mineur, pour
    consentement express relatif aux clauses ci-dessus énoncées.
</p>
